namespace Malsys {
	/// <summary>
	/// Helper with ASCII-safe definitions of some unicode characters.
	/// </summary>
	public static class CharHelper {

		/// <summary>
		/// √
		/// </summary>
		public const char Sqrt = '\u221a';

		/// <summary>
		/// Non-breaking space ` `.
		/// </summary>
		public const char Nbsp = '\u00A0';

		/// <summary>
		/// π
		/// </summary>
		public const char Pi = '\u03C0';

		/// <summary>
		/// τ
		/// </summary>
		public const char Tau = '\u03C4';

		/// <summary>
		/// φ
		/// </summary>
		public const char Phi = '\u03C6';

		/// <summary>
		/// ϕ
		/// </summary>
		public const char VarPhi = '\u03D5';

		/// <summary>
		/// ∞​
		/// </summary>
		public const char Infinity = '\u221E';

		/// <summary>
		/// ✰
		/// </summary>
		public const char ShadowedWhiteStar = '\u2730';

	}
}
